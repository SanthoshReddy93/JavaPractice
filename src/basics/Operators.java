package basics;

public class Operators {

	/*
	 * Arithmetic Operators 
	 * 
	 * +(Addition)
	 * 
	 * -(subtraction)
	 * 
	 * *(multiplication)
	 * 
	 * / (division) quoicent
	 * 
	 * % (modulus) remindar
	 * 
	 * unary operators 
	 * 
	 * ++ 
	 * 
	 * --
	 * 
	 */
	
	
	public static void main(String[] args) {
		
		int i = 2;
		int j = 1;
		
		System.out.println(i+j);
		System.out.println(i-j);
		System.out.println(i*j);
		System.out.println(i/j);
		System.out.println(i%j);
		
		
		
	}

}
